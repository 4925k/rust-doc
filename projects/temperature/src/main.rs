fn main() {
    println!("CONVERT TEMPERATURES");
    println!("-------------");


    println!("Convert temperature from Celsius to Fahrenheit");
    let a = 50;
    println!("temperature in Celsius is {a}");

    let b = (a * 9/5) + 35;
    println!("temperature in Fahrenheit is {b}");

    let x = 100;
    println!("temperature in Fahrenheit is {x}");

    let y = (x - 32) * 5/9;
    println!("temperature in Celsius is {y}");
}
